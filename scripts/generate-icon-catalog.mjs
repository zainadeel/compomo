#!/usr/bin/env node
/**
 * Generate lazy icon-loader catalogs for ds-icon.
 *
 * Each entry is a static-analyzable dynamic import (`() => import('@ds-mo/icons/svg/Bell')`)
 * so consumer bundlers code-split one tiny chunk per icon instead of carrying the whole
 * catalog (~340 kB raw) in the initial bundle. Glyphs load on demand, cache in the shared
 * icon cache (see icon-cache.ts), and apps can pre-register critical icons with
 * `registerIcons` for synchronous first paint.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT_DIR = path.join(ROOT, 'src', 'wc', 'components', 'Icon');
const require = createRequire(import.meta.url);

const metaPath = require.resolve('@ds-mo/icons/meta.json');
const meta = JSON.parse(fs.readFileSync(metaPath, 'utf8'));

function writeCatalog(filename, exportName, icons, importPrefix) {
  const entries = icons
    .map(i => `  ${i.name}: () => import('${importPrefix}/${i.name}').then(m => m.${i.name}),`)
    .join('\n');

  const content = `/** Auto-generated by scripts/generate-icon-catalog.mjs — do not edit. */

export const ${exportName}: Record<string, () => Promise<string>> = {
${entries}
};
`;

  const outputPath = path.join(OUT_DIR, filename);
  if (fs.existsSync(outputPath) && fs.readFileSync(outputPath, 'utf8') === content) return;
  fs.writeFileSync(outputPath, content);
}

const system = meta.icons.filter(i => i.category === 'system');
const flags = meta.icons.filter(i => i.category === 'flag');

writeCatalog('system-icon-catalog.ts', 'systemIconLoaders', system, '@ds-mo/icons/svg');
writeCatalog('flag-icon-catalog.ts', 'flagIconLoaders', flags, '@ds-mo/icons/svg/flags');

console.log(
  `  Verified lazy icon-loader catalogs (${system.length} system, ${flags.length} flag) from @ds-mo/icons@${meta.version}`,
);
