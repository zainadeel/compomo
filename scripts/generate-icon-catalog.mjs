#!/usr/bin/env node
/**
 * Generate static icon catalogs for ds-icon.
 *
 * Consumer bundlers tree-shake `import * as icons` + dynamic `icons[name]` in production.
 * Named imports + object literals give each IcoMo module a static edge so prod builds
 * retain the full catalog the peer @ds-mo/icons version provides.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT_DIR = path.join(ROOT, 'src', 'wc', 'components', 'Icon');
const require = createRequire(import.meta.url);

const metaPath = require.resolve('@ds-mo/icons/meta.json');
const meta = JSON.parse(fs.readFileSync(metaPath, 'utf8'));

function writeCatalog(filename, exportName, icons, importPrefix) {
  const importLines = icons.map(i => `import { ${i.name} } from '${importPrefix}/${i.name}';`);
  const entries = icons.map(i => `  ${i.name},`).join('\n');

  const content = `/** Auto-generated by scripts/generate-icon-catalog.mjs — do not edit. */
${importLines.join('\n')}

export const ${exportName}: Record<string, string> = {
${entries}
};
`;

  fs.writeFileSync(path.join(OUT_DIR, filename), content);
}

const system = meta.icons.filter(i => i.category === 'system');
const flags = meta.icons.filter(i => i.category === 'flag');

writeCatalog('system-icon-catalog.ts', 'systemIconCatalog', system, '@ds-mo/icons/svg');
writeCatalog('flag-icon-catalog.ts', 'flagIconCatalog', flags, '@ds-mo/icons/svg/flags');

console.log(
  `  Generated icon catalogs (${system.length} system, ${flags.length} flag) from @ds-mo/icons@${meta.version}`,
);
